#week days display
day1=int(input("ENTER THE NUMBER BETWEEN 1 TO 7: "))
if day1==1:
    print("SUNDAY")
elif day1==2:
    print("MONDAY")
elif day1==3:
    print("TUESDAY")
elif day1==4:
    print("WEDNESDAY")
elif day1==5:
    print("THURSDAY")
elif day1==6:
    print("FRIDAY")
elif day1==7:
    print("SATURDAY")
else:
    print("INVALID NUMBER!")